class SemanticChunker:

    def __init__(self, config: Optional[ChunkingConfig] = None):
        self.config = config or ChunkingConfig()

        if tiktoken:
            self.tokenizer = tiktoken.get_encoding(self.config.encoding_name)
        else:
            self.tokenizer = None
            logger.warning("tiktoken not available → fallback mode")

    # ======================================================
    # PUBLIC API
    # ======================================================

    def chunk_document(self, document) -> List:
        text = (document.content or "").strip()

        if not text:
            return []

        parent_chunks = self._split_by_tokens(
            text,
            self.config.parent_chunk_size,
            self.config.parent_chunk_overlap
        )

        results = []

        for parent_idx, parent in enumerate(parent_chunks):

            parent_id = str(uuid4())

            child_chunks = (
                self._split_by_tokens(
                    parent,
                    self.config.chunk_size,
                    self.config.chunk_overlap
                )
                if self.config.enable_parent_child
                else [parent]
            )

            for child_idx, chunk_text in enumerate(child_chunks):

                token_count = self._count_tokens(chunk_text)

                if token_count < self.config.min_chunk_length_tokens:
                    continue

                chunk = DocumentChunk(
                    document_id=document.id,
                    tenant_id=document.tenant_id,
                    content=chunk_text.strip(),
                    chunk_index=child_idx,
                    parent_chunk_id=parent_id if self.config.enable_parent_child else None,
                    security_level=document.security_level,
                    metadata={
                        **document.metadata,
                        "parent_index": parent_idx,
                    },
                )

                chunk.token_count = token_count
                results.append(chunk)

        logger.info(
            "document_chunked",
            extra={
                "document_id": document.id,
                "chunks": len(results),
            },
        )

        return results

    # ======================================================
    # TOKEN SPLITTING (CLEAN + SAFE)
    # ======================================================

    def _split_by_tokens(self, text: str, max_tokens: int, overlap: int) -> List[str]:

        # تنظيف بسيط قبل التقسيم
        text = re.sub(r'\s+', ' ', text).strip()

        sentences = re.split(r'(?<=[.!?])\s+', text)

        chunks = []
        current = []
        current_tokens = 0

        for sentence in sentences:

            sentence_tokens = self._count_tokens(sentence)

