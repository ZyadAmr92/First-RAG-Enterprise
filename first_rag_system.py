# PII Redaction Patterns
# ==========================================================

class PIIUtils:
    EMAIL_PATTERN = re.compile(r"[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}")
    API_KEY_PATTERN = re.compile(r"(sk-[A-Za-z0-9]{20,}|AIzaSy[A-Za-z0-9-_]{33})")
    CREDIT_CARD_PATTERN = re.compile(r"\b(?:\d[ -]*?){13,16}\b")

    @classmethod
    def redact_text(cls, text: str) -> str:
        if not text:
            return text
        text = cls.EMAIL_PATTERN.sub("[REDACTED_EMAIL]", text)
        text = cls.API_KEY_PATTERN.sub("[REDACTED_API_KEY]", text)
        text = cls.CREDIT_CARD_PATTERN.sub("[REDACTED_CARD]", text)
        return text

class PIIRedactionFilter(logging.Filter):
    def filter(self, record):
        # جلب الرسالة مدمجاً معها الـ args إن وجدت
        message = str(record.getMessage())
        # حجب البيانات
        record.msg = PIIUtils.redact_text(message)
        # تفريغ الـ args تماماً لمنع الفورمات المزدوج أو الأخطاء البرمجية
        record.args = ()
        return True

