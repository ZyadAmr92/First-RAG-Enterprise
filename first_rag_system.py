class ContextualChunkerOptimized:

    def __init__(self, config, embedder):
        self.config = config
        self.embedder = embedder  # ⌁ injected once (NO repeated calls)

    # ------------------------------------------------------
    def _tokens(self, text: str) -> int:
        return max(1, len(text.split()))

    # ------------------------------------------------------
    def _cosine(self, a: np.ndarray, b: np.ndarray) -> float:
        return float(np.dot(a, b) / (np.linalg.norm(a) * np.linalg.norm(b)))

    # ------------------------------------------------------
    def chunk_document(self, document) -> List[str]:

        text = re.sub(r"\s+", " ", document.content).strip()
        sentences = re.split(r'(?<=[.!?])\s+', text)

        if not sentences:
            return []

        # ==================================================
        # 1. PRECOMPUTE EMBEDDINGS (⌁ MAIN OPTIMIZATION)
        # ==================================================
        embeddings = self.embedder.encode(sentences, normalize_embeddings=True)

        chunks = []
        current_chunk = []
        current_embs = []

        # running centroid for fast similarity
        centroid = None

        for i, sent in enumerate(sentences):

            sent_emb = embeddings[i]

            if not current_chunk:
                current_chunk.append(sent)
                current_embs.append(sent_emb)
                centroid = sent_emb
                continue

            # update centroid
            centroid = np.mean(current_embs, axis=0)
            sim_centroid = self._cosine(centroid, sent_emb)
            sim_last = self._cosine(current_embs[-1], sent_emb)

            sim = (0.5 * sim_centroid) + (0.5 * sim_last)

            # – NEW: topic shift detection
            topic_shift = sim_last < 0.45

            projected_tokens = self._tokens(" ".join(current_chunk + [sent]))

            # ==================================================
            # DECISION
            # ==================================================
            if (
                not topic_shift
                and sim >= self.config.similarity_threshold
                and projected_tokens <= self.config.max_chunk_tokens
            ):
                current_chunk.append(sent)
                current_embs.append(sent_emb)
            else:
                chunks.append(" ".join(current_chunk))
                current_chunk = [sent]
                current_embs = [sent_emb]
                centroid = sent_emb

        if current_chunk:
            chunks.append(" ".join(current_chunk))

        # ==================================================
        # FILTER SMALL CHUNKS
        # ==================================================
        chunks = [
            c for c in chunks
            if self._tokens(c) >= self.config.min_chunk_tokens
        ]

        return chunks

