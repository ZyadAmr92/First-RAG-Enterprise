def simple_similarity(a: str, b: str) -> float:
    """Lightweight semantic approximation (no heavy model)."""
    set_a = set(a.lower().split())
    set_b = set(b.lower().split())
    if not set_a or not set_b:
        return 0.0
    return len(set_a & set_b) / len(set_a | set_b)


