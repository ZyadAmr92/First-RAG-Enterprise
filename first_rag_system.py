# JSON Formatter
# ==========================================================

class JsonFormatter(logging.Formatter):
    def format(self, record):
        payload = {
            "timestamp": self.formatTime(record),
            "level": record.levelname,
            "logger": record.name,
            "message": record.getMessage(),
            "module": record.module,
            "function": record.funcName,
            "line": record.lineno,
            "request_id": getattr(record, "request_id", None),
            "tenant_id": getattr(record, "tenant_id", None),
            "user_id": getattr(record, "user_id", None),
            "trace_id": getattr(record, "trace_id", None),
        }

        # تجميع الـ extra fields تلقائياً باستثناء الحقول الأساسية لبايثون
        extra_fields = {}
        for key, value in record.__dict__.items():
            if key.startswith("_"):
                continue
            if key in {
                "args", "msg", "message", "pathname", "filename", "module",
                "exc_info", "exc_text", "stack_info", "lineno", "funcName",
                "created", "msecs", "relativeCreated", "thread", "threadName",
                "processName", "process", "name", "levelname", "levelno",
                "request_id", "tenant_id", "user_id", "trace_id"
            }:
                continue
            extra_fields[key] = value

        if extra_fields:
            payload["extra"] = extra_fields

        # التعامل الآمن وحجب الـ PII من الـ Exceptions والـ Tracebacks
        if record.exc_info:
            exc_text = self.formatException(record.exc_info)
            payload["exception"] = PIIUtils.redact_text(exc_text)

        return json.dumps(payload, cls=SafeJsonEncoder, ensure_ascii=False)

