# ==========================================================
# Metadata Extractor (Enterprise Grade)
# ==========================================================

class MetadataExtractor:

    def __init__(self, config: ExtractionConfig = ExtractionConfig()):
        self.config = config

        raw_department_patterns = {
            "legal": r"\b(law|attorney|court|contract|compliance|policy|agreement)\b",
            "finance": r"\b(revenue|invoice|budget|tax|profit|loss|audit|payment)\b",
            "engineering": r"\b(api|code|system|architecture|microservice|kubernetes|database)\b",
            "hr": r"\b(hiring|employee|salary|benefits|recruitment|talent)\b",
            "sales": r"\b(deal|client|pipeline|revenue|crm|marketing)\b",
        }

        self.department_patterns = {
            k: re.compile(v, re.IGNORECASE)
            for k, v in raw_department_patterns.items()
        }

        self.pii_patterns = {
            "email": re.compile(r"[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}"),
            "phone": re.compile(r"\+?\d[\d\s\-]{9,}"),
            "id_like": re.compile(r"\b[A-Z0-9]{8,}\b"),
        }

        self.PROTECTED_FIELDS: Set[str] = {
            "tenant_id", "security_level", "source", "processed_at"
        }

        # Simple in-memory cache (enterprise upgrade can be Redis)
        self._cache: Dict[str, Dict[str, Any]] = {}

    # ==========================================================
    # Public API
    # ==========================================================

    def extract(
        self,
        text: Optional[str],
        source: str,
        existing_meta: Optional[Dict[str, Any]] = None,
        tenant_id: str = "default",
        security_level: str = "internal",
    ) -> Dict[str, Any]:

        safe_text = (text or "").strip()
        text_hash = hashlib.sha256(safe_text.encode()).hexdigest()

        # ======================================================
        # Cache Layer (optional)
        # ======================================================
        if self.config.enable_caching and text_hash in self._cache:
            return self._cache[text_hash]

        start_time = datetime.now(timezone.utc)

        base_meta: Dict[str, Any] = {
            "source": source,
            "tenant_id": tenant_id,
            "security_level": security_level,
            "processed_at": start_time.isoformat(),
            "char_count": len(safe_text),
            "word_count": len(safe_text.split()) if safe_text else 0,
        }

        # ======================================================
        # Enrichment
        # ======================================================
        if len(safe_text) >= self.config.min_text_length_for_detection:

            if self.config.enable_language_detection:
                lang = self._detect_language(safe_text)
                base_meta["language"] = lang

            if self.config.enable_department_detection:
                dept, confidence = self._guess_department(safe_text)
                base_meta["department"] = dept
                base_meta["department_confidence"] = confidence

            if self.config.enable_pii_flags:
                base_meta["pii_flags"] = self._detect_pii(safe_text)

        else:
            base_meta.update({
                "language": "unknown",
                "department": "general",
                "pii_flags": []
            })

        # ======================================================
        # Safe Merge
        # ======================================================
        if existing_meta:
            clean = {
                k: v for k, v in existing_meta.items()
                if k not in self.PROTECTED_FIELDS
            }
            base_meta.update(clean)

        # ======================================================
        # Logging
        # ======================================================
        logger.info(
            "metadata_extracted",
            extra={
                "source": source,
                "tenant_id": tenant_id,
                "department": base_meta.get("department"),
                "language": base_meta.get("language"),
                "pii": bool(base_meta.get("pii_flags")),
            },
        )

        # Cache store
        if self.config.enable_caching:
            self._cache[text_hash] = base_meta

        return base_meta

    # ==========================================================
    # Language
    # ==========================================================

    def _detect_language(self, text: str) -> str:
        if not langdetect:
            return "unknown"
        try:
            return langdetect.detect(text)
        except Exception:
            return "unknown"

    # ==========================================================
    # Department (with confidence scoring)
    # ==========================================================

    def _guess_department(self, text: str) -> Tuple[str, float]:

        scores: Dict[str, int] = {}

        for dept, pattern in self.department_patterns.items():
            matches = pattern.findall(text)
            scores[dept] = len(matches)

        if not scores or max(scores.values()) == 0:
            return "general", 0.0

        best = max(scores, key=scores.get)
        total = sum(scores.values())

        confidence = scores[best] / total if total else 0.0

        return best, round(confidence, 3)

    # ==========================================================
    # PII Detection
    # ==========================================================

    def _detect_pii(self, text: str) -> List[str]:
        return [
            k for k, v in self.pii_patterns.items()
            if v.search(text)
        ]

