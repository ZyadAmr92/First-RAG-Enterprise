# STAGE 10.5 — ADVANCED ENTERPRISE EMBEDDING PIPELINE
# =====================================================

class AdvancedEmbeddingEngine:
    def __init__(self, embedding_manager):
        self.embedding_manager = embedding_manager
        self.stats = {}

    # --------------------------------------------
    # Dynamic Batch Size
    # --------------------------------------------
    def choose_batch_size(self, n_chunks):
        if n_chunks < 50:
            return 8
        elif n_chunks < 500:
            return 16
        elif n_chunks < 5000:
            return 32
        return 64

    # --------------------------------------------
    # Semantic Enrichment
    # --------------------------------------------
    def enrich_chunk(self, chunk):
        content = getattr(chunk, "content", "")

        metadata = getattr(chunk, "metadata", {}) or {}

        source = metadata.get("source", "unknown_document")
        section = metadata.get("section", "general")

        enriched = f"""
Document: {source}
Section: {section}
Content:
{content}
"""
        return enriched.strip()

    # --------------------------------------------
    # Duplicate Detection
    # --------------------------------------------
    def detect_duplicates(self, chunks):
        hashes = []
        duplicates = 0

        for chunk in chunks:
            text = getattr(chunk, "content", "")
            h = hashlib.sha256(text.encode()).hexdigest()
            hashes.append(h)

        counts = Counter(hashes)

        for _, c in counts.items():
            if c > 1:
                duplicates += c - 1

        duplicate_ratio = duplicates / len(chunks) if chunks else 0
        return duplicates, duplicate_ratio

    # --------------------------------------------
    # Validation
    # --------------------------------------------
    def validate_embedding(self, vec):
        vec = np.array(vec)

        if np.isnan(vec).any():
            raise ValueError("Embedding contains NaN")

        if np.isinf(vec).any():
            raise ValueError("Embedding contains Inf")

        if np.linalg.norm(vec) == 0:
            raise ValueError("Zero vector detected")

        return True

    # --------------------------------------------
    # Quality Metrics
    # --------------------------------------------
    def compute_quality_metrics(self, chunks, elapsed):
        vectors = [
            np.array(chunk.embedding)
            for chunk in chunks
            if hasattr(chunk, "embedding") and chunk.embedding is not None
        ]

        norms = [np.linalg.norm(v) for v in vectors]

        metrics = {
            "embedded_chunks": len(vectors),
            "latency_sec": round(elapsed, 3),
            "throughput_chunks_per_sec": round(len(vectors) / elapsed, 2) if elapsed else 0,
            "avg_vector_norm": round(float(np.mean(norms)), 4) if norms else 0
        }

        return metrics

    # --------------------------------------------
    # Main Pipeline
    # --------------------------------------------
    def process(self, chunks):
        if not chunks:
            raise ValueError("No chunks found. Run chunking stage first.")

        duplicates, dup_ratio = self.detect_duplicates(chunks)

        batch_size = self.choose_batch_size(len(chunks))

        if hasattr(self.embedding_manager, "config"):
            self.embedding_manager.config.batch_size = batch_size

        enriched_texts = []

        for chunk in chunks:
            enriched = self.enrich_chunk(chunk)
            enriched_texts.append(enriched)

        start = time.time()

        embeddings = self.embedding_manager.embed_texts(enriched_texts)

        for chunk, emb in zip(chunks, embeddings):
            self.validate_embedding(emb)
            chunk.embedding = emb

            if not hasattr(chunk, "metadata"):
                chunk.metadata = {}

            chunk.metadata["advanced_embedding"] = True
            chunk.metadata["validated"] = True

        elapsed = time.time() - start

        quality = self.compute_quality_metrics(chunks, elapsed)

        quality["duplicates_found"] = duplicates
        quality["duplicate_ratio"] = round(dup_ratio, 4)
        quality["dynamic_batch_size"] = batch_size

        self.stats = quality

        return chunks


