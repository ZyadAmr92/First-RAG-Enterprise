    def insert_chunks(self, chunks: List[Any]):

        points = []

        for chunk in chunks:

            metadata = getattr(chunk, "metadata", {}) or {}

            points.append(
                PointStruct(
                    id=str(uuid.uuid4()),
                    vector=getattr(chunk, "embedding", None),
                    payload={
                        "content": getattr(chunk, "content", ""),
                        "source": metadata.get("source", "unknown"),
                        "tenant_id": metadata.get("tenant_id", "default"),

                        # 🔥 IMPORTANT FOR STAGE 13 ROUTING
                        "domain": metadata.get("domain", "general"),
                        "section": metadata.get("section", "general")
                    }
                )
            )

        # remove invalid vectors
        points = [p for p in points if p.vector is not None]

        self.client.upsert(
            collection_name=self.config.collection_name,
            points=points
        )

        print(f"Inserted vectors: {len(points)}")

    # -----------------------------
    def search(self, query_embedding, top_k=5, domain: Optional[str] = None):

        query_filter = None

        if domain:
            query_filter = Filter(
                must=[
                    FieldCondition(
                        key="domain",
                        match=MatchValue(value=domain)
                    )
                ]
            )

        results = self.client.query_points(
            collection_name=self.config.collection_name,
            query=query_embedding,
            limit=top_k,
            query_filter=query_filter
        )

