class SafeJsonEncoder(json.JSONEncoder):
    """يمنع انهيار النظام إذا تم تمرير كائنات معقدة في الـ extra fields"""
    def default(self, obj):
        try:
            if isinstance(obj, Path):
                return str(obj)
            return super().default(obj)
        except TypeError:
            return str(obj)

