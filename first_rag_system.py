def _l2_normalize(vec: Sequence[float]) -> List[float]:
    norm = sqrt(sum(x * x for x in vec))
    if norm == 0:
        return list(vec)
    return [x / norm for x in vec]


def _batch(items: Sequence[Any], batch_size: int) -> Iterable[List[Any]]:
    for i in range(0, len(items), batch_size):
        yield list(items[i:i + batch_size])


def _text_hash(provider_name: str, model_name: str, text: str) -> str:
    payload = f"{provider_name}::{model_name}::{text}".encode("utf-8")
    return sha256(payload).hexdigest()


