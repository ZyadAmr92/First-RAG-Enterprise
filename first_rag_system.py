class EnterpriseReranker:

    def rerank(self, query, results, top_k=5):

        query_terms = set(query.lower().split())

        rescored = []

        for r in results:
            text = r["content"].lower()

            keyword_hits = sum(
                1 for t in query_terms if t in text
            )

            base_score = r.get("score", 0)

            final_score = (
                base_score * 0.7 +
                keyword_hits * 0.3
            )

            new_r = dict(r)
            new_r["rerank_score"] = final_score

            rescored.append(new_r)

        rescored.sort(
            key=lambda x: x["rerank_score"],
            reverse=True
        )

        return rescored[:top_k]