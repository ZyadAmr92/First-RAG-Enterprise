from dataclasses import dataclass


@dataclass
class GeneratorConfig:
    max_context_chars: int = 8000


class AnswerGenerator:

    def __init__(self, config=None):
        self.config = config or GeneratorConfig()

    def generate(self, query, retrieval_results):

        context = []

        if isinstance(retrieval_results, dict):
            if "dense_results" in retrieval_results:
                docs = retrieval_results["dense_results"]
            elif "dense" in retrieval_results:
                docs = retrieval_results["dense"]
            else:
                docs = []
        else:
            docs = retrieval_results

        for r in docs:
            content = r.get("content", "")
            context.append(content)

        merged_context = "\n".join(context)
        merged_context = merged_context[:self.config.max_context_chars]

        if not merged_context.strip():
            return "No relevant context found."

        if query.lower() in merged_context.lower():
            return f"Direct match found for query: {query}\n\n{merged_context[:1200]}"

        return (
            f"Answer based on retrieved context:\n\n"
            f"{merged_context[:1500]}"
        )

