class EnterpriseSemanticChunkerV2:

    def __init__(self, config: ChunkingConfig):
        self.config = config
        self.seen_hashes = set()

    # ----------------------------
    # QUALITY SCORING
    # ----------------------------
    def _score_chunk(self, text: str) -> float:
        words = text.lower().split()

        if not words:
            return 0.0

        uniqueness = len(set(words)) / len(words)
        length_score = min(len(words) / 200, 1.0)

        return (uniqueness * 0.6) + (length_score * 0.4)

    # ----------------------------
    # DEDUP CHECK
    # ----------------------------
    def _is_duplicate(self, text: str) -> bool:
        h = hashlib.md5(text.encode()).hexdigest()

        if h in self.seen_hashes:
            return True

        self.seen_hashes.add(h)
        return False

    # ----------------------------
    # MAIN SPLITTER
    # ----------------------------
    def chunk_document(self, document) -> List[DocumentChunk]:

        text = clean_text(document.content)
        sentences = re.split(r'(?<=[.!?])\s+', text)

        chunks = []
        current = []
        current_tokens = 0
        idx = 0

        for sent in sentences:

            sent_tokens = count_tokens(sent)

            # semantic boundary check (light)
            if current:
                sim = simple_similarity(current[-1], sent)

                if sim < self.config.semantic_split_threshold:
                    self._flush_chunk(
                        chunks,
                        current,
                        idx,
                        document
                    )
                    idx += 1
                    current = []
                    current_tokens = 0

            # overflow check
            if current_tokens + sent_tokens > self.config.chunk_size:
                self._flush_chunk(
                    chunks,
                    current,
                    idx,
                    document
                )
                idx += 1
                current = []
                current_tokens = 0

            current.append(sent)
            current_tokens += sent_tokens

        # flush last
        if current:
            self._flush_chunk(
                chunks,
                current,
                idx,
                document
            )

        return chunks

    # ----------------------------
    # FLUSH LOGIC
    # ----------------------------
    def _flush_chunk(self, chunks, sentences, idx, document):

        text = clean_text(" ".join(sentences))

        if not text:
            return

        token_count = count_tokens(text)

        # filter small chunks
        if token_count < self.config.min_chunk_tokens:
            return

        # deduplication
        if self.config.enable_deduplication and self._is_duplicate(text):
            return

        quality = self._score_chunk(text)

        # quality filter
        if self.config.enable_quality_filter and quality < 0.25:
            return

        chunk = DocumentChunk(
            content=text,
            chunk_index=idx,
            token_count=token_count,
            quality_score=quality,
            metadata={
                "doc_id": document.id,
                "source": document.source,
                "tenant_id": getattr(document, "tenant_id", "default")
            }
        )

        chunks.append(chunk)


