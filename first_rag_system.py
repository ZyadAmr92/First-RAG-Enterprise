class BaseLoader(ABC):

    def __init__(
        self,
        source: Union[str, Path],
        tenant_id: str = "default",
        security_level: str = "internal",
    ):
        self.source = str(source)
        self.tenant_id = tenant_id
        self.security_level = security_level

        # تفعيل الـ Validation فوراً عند إنشاء الكائن لمنع أي خطأ مبكر
        self.validate_source()

    # =====================================================
    # Context Manager
    # =====================================================

    def __enter__(self):
        logger.info(
            "loader_started",
            extra={
                "loader": self.__class__.__name__,
                "source": self.source,
                "tenant_id": self.tenant_id,
            },
        )
        return self

    def __exit__(self, exc_type, exc_val, exc_tb):
        if exc_val:
            logger.exception(
                "loader_failed",
                extra={
                    "loader": self.__class__.__name__,
                    "source": self.source,
                },
            )
        else:
            logger.info(
                "loader_completed_successfully",
                extra={"loader": self.__class__.__name__},
            )
        return False  # السماح للـ Exceptions بالارتفاع بعد تسجيلها

    # =====================================================
    # Public API (Template Methods - Do Not Override)
    # =====================================================

    def load(self) -> List[EnterpriseDocument]:
        """
        الأسلوب العام لتحميل المستندات في دفعة واحدة.
        يقوم تلقائياً بالتحقق، الحساب الزمني، وحقن البيانات الأمنية.
        """
        start = perf_counter()

        # استدعاء البنية التحتية الخاصة بالمطور
        raw_docs = self._execute_load()

        # حقن البيانات الأمنية إجبارياً لضمان الـ Compliance
        enriched_docs = [self._enrich_document(doc) for doc in raw_docs]

        elapsed = (perf_counter() - start) * 1000
        logger.info(
            "batch_load_complete",
            extra={
                "loader": self.__class__.__name__,
                "documents_count": len(enriched_docs),
                "latency_ms": round(elapsed, 2),
            },
        )
        return enriched_docs

    def lazy_load(self) -> Iterator[EnterpriseDocument]:
        """
        الأسلوب العام لقراءة البيانات كـ Stream مستمر.
        يحسب الأداء لكل وثيقة ويضمن عدم تسريب أي مستند بدون تأمين.
        """
        start = perf_counter()
        count = 0

        # استهلاك الـ Generator الخاص بالمطور وحقنه أوتوماتيكياً
        for doc in self._execute_lazy_load():
            count += 1
            yield self._enrich_document(doc)

        elapsed = (perf_counter() - start) * 1000
        logger.info(
            "stream_load_complete",
            extra={
                "loader": self.__class__.__name__,
                "total_documents_streamed": count,
                "total_latency_ms": round(elapsed, 2),
            },
        )

    # =====================================================
    # Abstract Methods (To Be Implemented By Subclasses)
    # =====================================================

    @abstractmethod
    def _execute_load(self) -> List[EnterpriseDocument]:
        """المطور يكتب هنا كود القراءة الفعلي للملف بالكامل"""
        raise NotImplementedError

    @abstractmethod
    def _execute_lazy_load(self) -> Iterator[EnterpriseDocument]:
        """المطور يكتب هنا كود الـ Generator (Yield) للقراءة السطرية"""
        raise NotImplementedError

    # =====================================================
    # Validation & Security (Enforced Guardrails)
    # =====================================================

    def validate_source(self):
        if not self.source or str(self.source).strip() == "":
            raise ValueError("Source cannot be empty or blank")

    def _enrich_document(self, doc: EnterpriseDocument) -> EnterpriseDocument:
        """حقن بيانات العميل والأمان بشكل صارم قبل خروج المستند من الـ Loader"""
        doc.tenant_id = self.tenant_id
        doc.security_level = self.security_level
        doc.source = self.source
        return doc

