class RetrievalOrchestrator:
    def __init__(self, hybrid_retriever, config):
        self.hybrid_retriever = hybrid_retriever
        self.config = config
        self.query_analyzer = QueryAnalyzer()

    def rerank(self, dense_results, sparse_results):
        merged = []
        seen = set()

        for r in dense_results:
            key = r["content"][:120]
            if key not in seen:
                merged.append({
                    "score": float(r["score"]) + 5,
                    "content": r["content"]
                })
                seen.add(key)

        for r in sparse_results:
            key = r["content"][:120]
            if key not in seen:
                merged.append({
                    "score": float(r["score"]),
                    "content": r["content"]
                })
                seen.add(key)

        merged.sort(key=lambda x: x["score"], reverse=True)
        return merged[:self.config.final_top_k]

    def compress_context(self, results):
        context = []

        for i, result in enumerate(results):
            text = result["content"]

            if len(text) > 800:
                text = text[:800]

            context.append(f"[Chunk {i+1}]\n{text}")

        final_context = "\n\n".join(context)
        return final_context[:self.config.max_context_chars]

    def retrieve(self, query):
        query_type = self.query_analyzer.analyze(query)

        # This is where hybrid_retriever is used and causes the NameError if not defined.
        results = self.hybrid_retriever.search(query)

        reranked = self.rerank(
            results["dense_results"],
            results["sparse_results"]
        )

        context = self.compress_context(reranked)

        return {
            "query_type": query_type,
            "context": context
        }


