class EmbeddingManager:
    """
    Responsible for:
    - batching
    - retries
    - caching
    - normalization
    - attaching embeddings + metadata back to chunks
    """

    def __init__(
        self,
        provider: EmbeddingProvider,
        config: Optional[EmbeddingConfig] = None,
    ):
        self.provider = provider
        self.config = config or EmbeddingConfig()
        self.cache: Dict[str, List[float]] = {}

    def _embed_batch_with_retry(self, texts: List[str]) -> List[List[float]]:
        last_exc = None

        for attempt in range(1, self.config.max_retries + 1):
            try:
                return self.provider.embed_documents(texts)
            except Exception as exc:
                last_exc = exc
                if attempt == self.config.max_retries:
                    break
                sleep(self.config.base_backoff_seconds * attempt)

        raise RuntimeError(
            f"Embedding failed after {self.config.max_retries} retries "
            f"using provider={self.provider.provider_name}, model={self.provider.model_name}"
        ) from last_exc

    def _cache_lookup(self, text: str) -> Optional[List[float]]:
        if not self.config.enable_cache:
            return None
        key = _text_hash(self.provider.provider_name, self.provider.model_name, text)
        return self.cache.get(key)

    def _cache_store(self, text: str, embedding: List[float]) -> None:
        if not self.config.enable_cache:
            return
        key = _text_hash(self.provider.provider_name, self.provider.model_name, text)
        self.cache[key] = embedding

    def embed_texts(self, texts: List[str]) -> List[List[float]]:
        if not texts:
            return []

        final_embeddings: List[Optional[List[float]]] = [None] * len(texts)
        missing_texts: List[str] = []
        missing_positions: List[int] = []

        # 1) cache hit
        for i, text in enumerate(texts):
            cached = self._cache_lookup(text)
            if cached is not None:
                final_embeddings[i] = cached
            else:
                missing_positions.append(i)
                missing_texts.append(text)

        # 2) provider inference in batches
        if missing_texts:
            start = perf_counter()
            generated: List[List[float]] = []

            for batch_texts in _batch(missing_texts, self.config.batch_size):
                batch_embeddings = self._embed_batch_with_retry(batch_texts)
                generated.extend(batch_embeddings)

            latency_ms = (perf_counter() - start) * 1000

            # 3) normalize + cache + place back
            for pos, text, emb in zip(missing_positions, missing_texts, generated):
                if self.config.normalize_embeddings:
                    emb = _l2_normalize(emb)

                if self.config.expected_dimension is not None and len(emb) != self.config.expected_dimension:
                    raise ValueError(
                        f"Embedding dimension mismatch: expected {self.config.expected_dimension}, got {len(emb)}"
                    )

                self._cache_store(text, emb)
                final_embeddings[pos] = emb

            if "logger" in globals():
                logger.info(
                    "embedding_batch_complete",
                    extra={
                        "provider": self.provider.provider_name,
                        "model": self.provider.model_name,
                        "count": len(texts),
                        "cached": len(texts) - len(missing_texts),
                        "generated": len(missing_texts),
                        "latency_ms": round(latency_ms, 2),
                    },
                )

        return [emb for emb in final_embeddings if emb is not None]

    def embed_chunks(self, chunks: List[Any]) -> List[Any]:
        if not chunks:
            return []

        texts = [getattr(chunk, "content", "") or "" for chunk in chunks]

        # Safety: skip empty strings rather than crashing provider calls
        non_empty_positions = [i for i, t in enumerate(texts) if t.strip()]
        non_empty_texts = [texts[i] for i in non_empty_positions]

        if not non_empty_texts:
            return chunks

        embeddings = self.embed_texts(non_empty_texts)

        for idx, emb in zip(non_empty_positions, embeddings):
            chunk = chunks[idx]
            chunk.embedding = emb

            # Attach rich metadata if the chunk supports it
            if not hasattr(chunk, "embedding_metadata") or chunk.embedding_metadata is None:
                try:
                    chunk.embedding_metadata = {}
                except Exception:
                    pass

            meta = {
                "provider": self.provider.provider_name,
                "model_name": self.provider.model_name,
                "embedding_version": self.config.embedding_version,
                "dimension": len(emb),
                "normalized": self.config.normalize_embeddings,
            }

            if hasattr(chunk, "embedding_metadata"):
                try:
                    chunk.embedding_metadata.update(meta)
                except Exception:
                    pass

            if hasattr(chunk, "metadata") and isinstance(chunk.metadata, dict):
                chunk.metadata.update({
                    "embedding_provider": self.provider.provider_name,
                    "embedding_model": self.provider.model_name,
                    "embedding_version": self.config.embedding_version,
                    "embedding_dimension": len(emb),
                })

        return chunks


