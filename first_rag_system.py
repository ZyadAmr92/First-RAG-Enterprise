@dataclass
class ChunkQualityMetrics:
    quality_score: float
    token_count: int
    lexical_diversity: float
    duplicate_ratio: float


class EnterpriseChunkEvaluator:

    def __init__(self):
        self.hashes = set()

    def evaluate(self, chunk_text: str):

        words = chunk_text.lower().split()

        token_count = len(words)

        if token_count == 0:
            return ChunkQualityMetrics(
                0.0, 0, 0.0, 1.0
            )

        lexical_diversity = (
            len(set(words))
            / token_count
        )

        chunk_hash = hashlib.md5(
            chunk_text.encode()
        ).hexdigest()

        duplicate_ratio = (
            1.0
            if chunk_hash in self.hashes
            else 0.0
        )

        self.hashes.add(chunk_hash)

        quality_score = (
            lexical_diversity * 0.5
            + min(token_count / 300, 1.0) * 0.5
        )

        quality_score *= (
            1 - duplicate_ratio
        )

        return ChunkQualityMetrics(
            quality_score,
            token_count,
            lexical_diversity,
            duplicate_ratio
        )


class RetrievalFusionEngine:

    @staticmethod
    def reciprocal_rank_fusion(
        dense_results,
        sparse_results,
        k=60
    ):

        scores = {}

        for rank, item in enumerate(
            dense_results,
            start=1
        ):
            key = item["content"]

            scores[key] = (
                scores.get(key, 0)
                + 1 / (k + rank)
            )

        for rank, item in enumerate(
            sparse_results,
            start=1
        ):
            key = item["content"]

            scores[key] = (
                scores.get(key, 0)
                + 1 / (k + rank)
            )

        ranked = sorted(
            scores.items(),
            key=lambda x: x[1],
            reverse=True
        )

        return ranked


